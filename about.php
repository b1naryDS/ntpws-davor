        As a 'rock inkie', it was the first British newspaper to include a singles chart,
        in the edition of 14 November 1952. In the 1970s, it became the best-selling British music newspaper.
        From 1972 to 1976, it was particularly associated with gonzo journalism
         then became closely associated with punk rock through the writings of Julie Burchill, Paul Morley, and Tony Parsons. It started as a music newspaper,
         and gradually moved toward a magazine format during the 1980s and 1990s, changing from newsprint in 1998.
    </p>
